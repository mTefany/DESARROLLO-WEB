	MAQ_DESCRIPCION varchar(50),
	MAQ_FEC_COMPRA date,
	MAQ_EJERCICIO varchar(40),
	CONSTRAINT MAQUINA_pk PRIMARY KEY (MAQ_COD)
);

CREATE TABLE MANTENIMIENTO (
