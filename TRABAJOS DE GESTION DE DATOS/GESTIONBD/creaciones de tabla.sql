CREATE TABLE TIPO_CARGO (
	TIPO_ID smallint NOT NULL,
	TIPO_DETALLE varchar(30),
	CONSTRAINT TIPO_CARGO_pk PRIMARY KEY (TIPO_ID)
);

CREATE TABLE CLIENTE (
	CLI_CED integer NOT NULL,
	CLI_NOMBRE varchar(30),
	CLI_APELLIDO varchar(30),
	CLI_NACIMIENTO date,
	CLI_CORRE varchar(30),
	CLI_TIPO_SANGRE varchar(5),
	CLI_CELULAR integer,
	CLI_CEL_EMERG integer,
	TIPO_ID_TIPO_CLIENTE smallint NOT NULL,
	RUTI_CODE_RUTINA smallint NOT NULL,
	CONSTRAINT CLIENTE_pk PRIMARY KEY (CLI_CED)
);

CREATE TABLE PERSONAL (
	PER_CODE integer NOT NULL,
	PER_NOMBRE varchar(30),
	PER_APELLIDO varchar(30),
	PER_CORREO varchar(40),
	PER_CELULAR integer,
	PER_ENTRADA timestamp,
	PER_SALIDA timestamp,
	TIPO_ID_TIPO_CARGO smallint NOT NULL,
	CONSTRAINT PERSONAL_pk PRIMARY KEY (PER_CODE)
);

CREATE TABLE MAQUINA (
	MAQ_COD smallint NOT NULL,
